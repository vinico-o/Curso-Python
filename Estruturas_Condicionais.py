#As estruturas condicionais permitem alterar o fluxo de execucao de instrucoes, executando ou nao, uma istrucao de acordo com condiocoes

nota = 3

if(nota >= 8): #executa se condicao 1 for verdadeira
    print("Perfeito")

elif(nota >= 5): #executa se condicao 2 for verdadeira
    print("Na media")
    
else: #executa se nenhuma das condicoes forem satisfeitas
    print("Melhore.")